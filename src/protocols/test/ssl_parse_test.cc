char* snort_strdup(const char* str)
{
    return str ? strdup(str) : nullptr;
}

char* snort_strndup(const char* src, size_t n)
{
    if (!src)
        return nullptr;
    char* dst = new char[n + 1];
    memcpy(dst, src, n);
    dst[n] = '\0';
    return dst;
}
